/**
 * Returns true if all characters in the word have stroke data.
 */
export const isWordSupported = (word) => {
  const letters = word.split('');
  // Check if every letter (ignoring tashkeel for now if we were stripping it, but here input might have it)
  // For now, assuming raw letters.
  return letters.every(l => LETTER_DATA[l]);
};
